package Seccion_5_FlujoDeControl;

public class SentenciaSwitchCase {
    public static void main(String[] args) {

        int num = 3;

        switch(num){
            case 0: System.out.println("el num es cero");break;
            case 1: System.out.println("el num es uno");break;
            case 2: System.out.println("el num es dos");break;
            case 3: System.out.println("el num es tres");break;
        }
    }
}
